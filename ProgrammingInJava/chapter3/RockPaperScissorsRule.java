package ProgrammingInJava.chapter3;

public class RockPaperScissorsRule{
    public static void main(String[] args) {
        System.out.println("Rock Paper Scissors Rule\n");
        System.out.print("Players decide on different hand signals to represent");
        System.out.println("rock, paper, and scissors.\n");

        System.out.println("Players make a throw at the same time.\n");

        System.out.println("The hand signals thrown by the players are then compared");
        System.out.println("to the rules of the game to determine the winner:");

        System.out.println("\nRock dulls Scissors <Rock wins>.");
        System.out.println("\nScissors cuts Paper <Scissors wins>.");
        System.out.println("\nPaper covers Rock <Paper wins>.");

    }
}